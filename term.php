KEBIJAKAN UNTUK PEMBUATAN AKUN
==============================================================================================================================================================
<img src="assets/img/term.jpeg"></img><br>
1. Jangan main klarinet.<br>
2. Jangan pernah melambaikan lampu senter ke depan dan ke belakang terlalu cepat. Itu dianggap undangan.<br>
3. Jangan pernah berhenti menatap sekitar.<br>
4. Jangan makan keju, kecuali yang kotak.<br>
5. Jangan pakai topi sombrero.<br>
6. Jangan pakai baju bodoh.<br>
7. Jangan pakai rok panjang.<br>
8. Jangan pakai sepatu merah.<br>
9. Jangan berlagak seperti kera.<br>
10. Jika beruang sudah mendekat, buat gambar lingkaran besar untuk melindungi diri dari gigitan beruang. Usahakan jangan lonjong.<br>
11. Jangan lari.<br>
12. Jangan jalan pincang.<br>
13. Jangan merangkak.<br>